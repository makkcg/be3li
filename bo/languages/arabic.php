    function convert_number_to_words($number) {

        if (($number != floor($number)) && 2 > 0) {
            $part1 = $this->convert_part_of_number_to_words((int) $number);
            $after_point = (string) ($number - floor($number));
            $after_point = substr($after_point, 2, 2);
            $part2 = "";
            $strlen = strlen($after_point);
            for ($i = 0; $i < $strlen; $i++) {
                $char = substr($after_point, $i, 1);
                $part2 .= $this->convert_part_of_number_to_words((int) $char) . " ";
            }
            return $part1 . " فصلة " . $part2;
        } else {
            return $this->convert_part_of_number_to_words($number);
        }
    }

    private function convert_part_of_number_to_words($number) {
        if ($number == 0) {
            return "صفر";
        }
        if (($number > 999999999999)) {
            return "";
        }
        if (($number < 0)) {
            $number = -$number;
            return "سالب " . $this->convert_number_to_words($number);
        }
        $return = "";
        $english_format_number = number_format($number);
        $array_number = explode(',', $english_format_number);
        for ($i = 0; $i < count($array_number); $i++) {
            $place = count($array_number) - $i;
            $return .= $this->convert_number($array_number[$i], $place);
            if (isset($array_number[($i + 1)]) && $array_number[($i + 1)] > 0)
                $return .= ' و';
        }
        return $return;
    }

